CREATE TABLE LOJAS (
	CODIGO BIGINT(20) PRIMARY KEY AUTO_INCREMENT,
	CNPJ VARCHAR(19) NOT NULL,
	NOME VARCHAR(50) NOT NULL,
	NOTA BIGINT(20) NOT NULL DEFAULT 5
)ENGINE=InnoDB DEFAULT CHARSET=UTF8;

