CREATE TABLE VENDAS(

    CODIGO BIGINT(20) PRIMARY KEY AUTO_INCREMENT,
    CLIENTE_ID BIGINT(20),
    PRECO_TOTAL NUMERIC,
    PARCELAS INTEGER,
    
    CONSTRAINT `fk_cliente` FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTES (CODIGO)

)ENGINE=InnoDB DEFAULT CHARSET=UTF8;

